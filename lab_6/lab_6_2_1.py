# Ввод списка целых чисел с проверкой
while True:
    try:
        line = input("Введите элементы списка через пробел: ").strip()
        if line == "":
            print("Ошибка: список не должен быть пустым. Повторите ввод.")
            continue
        mass = list(map(int, line.split()))
        break
    except ValueError:
        print("Ошибка: ввод должен содержать только целые числа. Повторите ввод.")

# Проверка, что есть что удалять
if len(mass) == 0:
    print("Ошибка: список пуст, удаление невозможно.")
else:
    # Ввод индекса удаления с проверкой диапазона [0; len(mass)-1]
    while True:
        try:
            idx = int(input(f"Введите индекс удаления (от 0 до {len(mass) - 1}): "))
            if 0 <= idx <= len(mass) - 1:
                break
            print(f"Ошибка: индекс должен быть в диапазоне от 0 до {len(mass) - 1}.")
        except ValueError:
            print("Ошибка: нужно ввести целое число. Повторите ввод.")

    # Удаление  встроенными средствами
    mass.pop(idx)

    # Вывод изменённого списка
    print("Изменённый список:", mass)